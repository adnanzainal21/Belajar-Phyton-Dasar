# width and multilane 

# Data 

data_nama = "Andi karna"
data_umur = "21"
data_tinggi = 173.1
data_nomor_sepatu = 43

# string

data_string = f"nama = {data_nama}, umur = {data_umur} , tinggi = {data_tinggi} , nomor sepatu = {data_nomor_sepatu}"

print(data_string)

# multiline
data_string = f"nama = {data_nama}, \numur = {data_umur} , \ntinggi = {data_tinggi} , \nnomor sepatu = {data_nomor_sepatu}"

print(data_string)


# string multiline ( kutip triplate )
data_string = f"""

nama = {data_nama} 
umur = {data_umur}
tinggi = {data_tinggi}
nomor sepatu = {data_nomor_sepatu}

"""
print(data_string)
